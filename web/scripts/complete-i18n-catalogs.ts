function withPlaceholders(value: string, english: string): string {
  const placeholders = [...english.matchAll(/\{[a-zA-Z0-9_]+\}/g)].map(m => m[0]);
  if (!placeholders.length) return value;
  return `${value} ${placeholders.join(" ")}`.trim();
}

function isCorrupt(value: string | undefined): boolean {
  if (!value) return true;
  return /\uFFFD|\u00E2\u20AC|\u9239|\u9225|\u922B|\u9397|\u9983|\u9241|\u9514|\?{2,}/.test(value);
}

function formatObject(name: string, catalog: Record<string, string>) {
  const lines = Object.entries(catalog).map(([key, value]) => `  ${JSON.stringify(key)}: ${JSON.stringify(value)},`);
  return `import type { MessageKey } from "./en";\n\nconst ${name}: Record<MessageKey, string> = {\n${lines.join("\n")}\n};\n\nexport default ${name};\n`;
}

function varName(locale: string) {
  return locale.replace(/-([a-z])/gi, (_, c: string) => c.toUpperCase()).replace(/[^a-zA-Z0-9]/g, "");
}

